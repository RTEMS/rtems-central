import logging

from rtemsspec.items import ItemCache
from rtemsspec.specverify import SpecVerifier, verify
from rtemsspec.tests.util import create_item_cache_config_and_copy_spec, \
    get_and_clear_log


def test_no_root_type(caplog, tmpdir):
    item_cache_config = create_item_cache_config_and_copy_spec(
        tmpdir, "spec-verify")
    item_cache = ItemCache(item_cache_config)
    config = {}
    caplog.set_level(logging.INFO)
    info = verify(config, item_cache)
    assert get_and_clear_log(
        caplog) == """ERROR configuration has no root type"""
    assert info.critical == 0
    assert info.error == 1
    assert info.warning == 0
    assert info.info == 0
    assert info.debug == 0


def test_no_root_item(caplog, tmpdir):
    item_cache_config = create_item_cache_config_and_copy_spec(
        tmpdir, "spec-verify")
    item_cache = ItemCache(item_cache_config)
    config = {"root-type": "/nix"}
    caplog.set_level(logging.INFO)
    info = verify(config, item_cache)
    assert get_and_clear_log(
        caplog) == """ERROR root type item does not exist in item cache"""
    assert info.critical == 0
    assert info.error == 1
    assert info.warning == 0
    assert info.info == 0
    assert info.debug == 0
    verifier = SpecVerifier(item_cache, "/nix")
    info = verifier.verify("/blub")
    assert get_and_clear_log(
        caplog) == """ERROR root type item does not exist in item cache"""
    assert info.critical == 0
    assert info.error == 1
    assert info.warning == 0
    assert info.info == 0
    assert info.debug == 0


